namespace BigFish\Pmgw\Gateway\Response;

use Magento\Payment\Gateway\Data\PaymentDataObjectInterface;
use Magento\Payment\Gateway\Response\HandlerInterface;
use Magento\Sales\Model\Order\Payment;
use BigFish\PaymentGateway;
use BigFish\Pmgw\Gateway\Helper\Helper;

class FraudHandler implements HandlerInterface
{
    /**
     * Handles fraud messages
     *
     * @param array $handlingSubject
     * @param array $response
     * @return void
     */
    public function handle(array $handlingSubject, array $response)
    {
        if (is_array($response) && $response[Helper::RESULT_CODE] === PaymentGateway::RESULT_CODE_SUCCESS) {
            return;
        }

        if (!isset($handlingSubject['payment'])
            || !$handlingSubject['payment'] instanceof PaymentDataObjectInterface
        ) {
            throw new \InvalidArgumentException('Payment data object should be provided');
        }

        /** @var PaymentDataObjectInterface $paymentDO */
        $paymentDO = $handlingSubject['payment'];
        $payment = $paymentDO->getPayment();

        $payment->setAdditionalInformation(
            $response[Helper::RESULT_CODE],
            (array)$response[Helper::RESULT_MESSAGE]
        );

        /** @var $payment Payment */
        $payment->setIsFraudDetected(true);
    }
}
